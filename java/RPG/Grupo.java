
abstract class Grupo{
  private String gritoDeGuerra;
  private int qtdMembros;

  public String getGritoDeGuerra(){
    return gritoDeGuerra;
  }

  public int getQtdMembros(){
    return qtdMembros;
  }

  public void setGritoDeGuerra(String g){
    gritoDeGuerra = g;
  }

  public void setQtdMembros(int q){
    qtdMembros = q;
  }
  
}